// 等级判断函数
export type WordLevel = 'CET4' | 'CET6' | 'Advanced' | 'Unknown';

export function getWordLevel(word: string): WordLevel {
  const w = word.toLowerCase();
  if (CET4_WORDS.has(w)) return 'CET4';
  if (CET6_WORDS.has(w)) return 'CET6';
  if (ADVANCED_WORDS.has(w)) return 'Advanced';
  return 'Unknown';
}

// 映射到 UI 的 Difficulty 类型
export function levelToDifficulty(level: WordLevel): 'Beginner' | 'Intermediate' | 'Advanced' {
  switch (level) {
    case 'CET4': return 'Beginner';
    case 'CET6': return 'Intermediate';
    case 'Advanced': return 'Advanced';
    default: return 'Intermediate'; // Unknown 词默认中级
  }
}
